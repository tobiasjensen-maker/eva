import { createContext, useContext } from 'react';

// Lightweight demo i18n: t() looks the English string up in the Danish dictionary
// and falls back to the English original, so untranslated strings degrade gracefully.
export type Lang = 'en' | 'da';

export const LangContext = createContext<{ lang: Lang; setLang: (l: Lang) => void; t: (s: string) => string }>({
    lang: 'en',
    setLang: () => {},
    t: (s) => s,
});

export const useLang = () => useContext(LangContext);

export function translate(lang: Lang, s: string): string {
    return lang === 'da' ? DA[s] ?? s : s;
}

